public class Operadores {

	public static void main(String[] args) {
		
		int x = 1;
		System.out.println("Operador pós: " + x++); // operador pós-fixado irá imprimir 1
		
		int y = 1;
		System.out.println("Operador pré: " + ++y); // operador pré-fixado irá imprimir 2

	}

}